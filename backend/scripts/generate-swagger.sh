#!/bin/bash

# Скрипт для генерации Swagger документации с поддержкой переменных окружения

# Переходим в корневую директорию проекта
cd "$(dirname "$0")/.."

# Получаем хост из переменных окружения или используем значение по умолчанию
HOST=${SWAGGER_HOST:-"localhost:3000"}

echo "Генерация Swagger документации с хостом: $HOST"

# Удаляем старую документацию
rm -rf docs/*

# Генерируем новую документацию
swag init -g cmd/server/main.go -o docs

